def spec_prompt() -> str:
    """AI台本生成プロンプトに注入する機種スペック表 (正確な数値の根拠)。"""
    lines = [
        "",
        "【参考: 主要ジャグラー機種の公表スペック (この数値を必ず使うこと)】",
    ]
    for name, data in SPECS.items():
        s1 = data["settings"][1]
        s6 = data["settings"][6]
        lines.append(
            f"■{name}: 設定1 BIG1/{s1[0]} REG1/{s1[1]} 合算1/{s1[2]} {s1[3]}% / "
            f"設定6 BIG1/{s6[0]} REG1/{s6[1]} 合算1/{s6[2]} {s6[3]}%"
        )
        g = data.get("grape")
        if g:
            lines.append(f"  ぶどう: 設定1 約1/{g[1]} → 設定6 約1/{g[6]}")
        for p in data["points"]:
            lines.append(f"  ・{p}")
    lines.append(
        "台データ(big/reg/total)は上記スペック表の設定値またはその近傍の実戦値とし、"
        "どの機種のどの設定域を想定したかが台本から分かるようにすること。"
    )
    return "\n".join(lines)
